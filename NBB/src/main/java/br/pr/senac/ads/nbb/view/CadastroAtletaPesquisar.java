    // Método que vai listar todos os campos do banco de dados caso a pessoa não insira nada na hora de pesquisar
    private void listarCampos() {
        
        try {
            
            AtletaDAO objAtleta = new AtletaDAO();
            DefaultTableModel model = (DefaultTableModel) jTableBanco.getModel();
            model.setRowCount(0);
            
            ArrayList<Atleta> lista = objAtleta.listarTodos();
            
            for(int i = 0; i < lista.size(); i++) {
                model.addRow(new Object[]{
                    lista.get(i).getCodigo(),
                    lista.get(i).getNome(),
                    lista.get(i).getAltura(),
                    lista.get(i).getDataNascimento(),
                    lista.get(i).getPeso(),
                    lista.get(i).getTime(),
                    lista.get(i).getPosicaoTime(),
                    lista.get(i).getEnvergadura(),
                    lista.get(i).getClassificacaoNBB()
                });
            }
                    
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro no ListarCampos");
        }
        
    }
    
    // Método que vai retornar uma pesquisa com base no nome selecionado.
    private void listarPesquisa(String nome) {
        
        try {
            
            AtletaDAO objAtleta = new AtletaDAO();
            DefaultTableModel model = (DefaultTableModel) jTableBanco.getModel();
            model.setRowCount(0);
            
            ArrayList<Atleta> lista = objAtleta.listarPesquisaNome(nome);
            
            for(int i = 0; i < lista.size(); i++) {
                model.addRow(new Object[]{
                    lista.get(i).getCodigo(),
                    lista.get(i).getNome(),
                    lista.get(i).getAltura(),
                    lista.get(i).getDataNascimento(),
                    lista.get(i).getPeso(),
                    lista.get(i).getTime(),
                    lista.get(i).getPosicaoTime(),
                    lista.get(i).getEnvergadura(),
                    lista.get(i).getClassificacaoNBB()
                });
            }
                    
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro no ListarCampos");
        }
        
    }
    
    //Método que vai carregar as informações que trouxemos do banco de dados e setar no lado esquerdo aonde contém os TextFields
    private void carregarCampos() throws ParseException {
        DateFormat dt = new SimpleDateFormat("dd/MM/yyyy");
        dt.setLenient(false);
        
        int linha = jTableBanco.getSelectedRow();
        
        jTextFieldCodigo.setText(jTableBanco.getModel().getValueAt(linha, 0).toString());
        jTextFieldNome.setText(jTableBanco.getModel().getValueAt(linha, 1).toString());
        jTextFieldAltura.setText(jTableBanco.getModel().getValueAt(linha, 2).toString());
//        jTextFieldDataNascimento.setText(jTableBanco.getModel().getValueAt(linha, 3).toString());
        jTextFieldPeso.setText(jTableBanco.getModel().getValueAt(linha, 4).toString());
        jTextFieldTime.setText(jTableBanco.getModel().getValueAt(linha, 5).toString());
        jTextFieldPosicaoTime.setText(jTableBanco.getModel().getValueAt(linha, 6).toString());
        jTextFieldEnvergadura.setText(jTableBanco.getModel().getValueAt(linha, 7).toString());
        jTextFieldClassificacaoNBB.setText(jTableBanco.getModel().getValueAt(linha, 8).toString());

        //Formatando minha data
        String minhaData = dt.format((jTableBanco.getModel().getValueAt(linha, 3)));
        jTextFieldDataNascimento.setText(minhaData);
        
   }

