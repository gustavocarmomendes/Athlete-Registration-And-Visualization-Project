    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        AtletaController atletaController = new AtletaController();
        
        atletaController.setCodigo(jTextFieldCodigo.getText());
        atletaController.setNome(jTextFieldNome.getText());
        atletaController.setAltura(jTextFieldAltura.getText());
        atletaController.setDataNascimento(jTextFieldDataNascimento.getText());
        atletaController.setPeso(jTextFieldPeso.getText());
        atletaController.setTime(jTextFieldTime.getText());
        atletaController.setPosicaoTime(jTextFieldPosicaoTime.getText());
        atletaController.setEnvergadura(jTextFieldEnvergadura.getText());
        atletaController.setClassificacaoNBB(jTextFieldClassificacaoNBB.getText());
        
        try {
            atletaController.atualizar(
                    atletaController.getCodigo(),
                    atletaController.getNome(),
                    atletaController.getAltura(),
                    atletaController.getDataNascimento(),
                    atletaController.getPeso(),
                    atletaController.getTime(),
                    atletaController.getPosicaoTime(),
                    atletaController.getEnvergadura(),
                    atletaController.getClassificacaoNBB());
                    JOptionPane.showMessageDialog(this, "Atleta atualizado com sucesso!");
                    listarCampos();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Não foi possível salvar atleta!" + e.getLocalizedMessage());
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Data possui formato inválido!" + e.getLocalizedMessage());
        }
        
        bloquearCampos();
        
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    
